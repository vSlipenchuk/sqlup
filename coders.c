int decode_base64(unsigned char *d,unsigned char *s,int slen) {
int len=0,bit=0;
unsigned char b=0, c;
if (slen<0) slen=strlen(s);
while(slen>0)
  {
  int i;
  if (*s=='=') break;
  if (*s>32)
  {
  for(i=0;i<64;i++) if (*s==table_base64[i]) break;
  if (i==64) {
    printf("base64: unk symbol '%c' hex=%d\n",*s,*s);
    return -1;
    }
  c=i;
  for(i=0;i<6;i++,c<<=1)
    {
    b<<=1;
    if (c&32) b|=1;
    bit++;
    if (bit==8) { if (d) *d=b; bit=0; len++; if (d) d++; b=0;}
    }
  }
  s++; slen--;
  }
if (d) *d=0;
return len;
}

